export default function Sidebar({ open, onClose }: { open: boolean; onClose: () => void }) {
  const pathname = usePathname();
  const [counts, setCounts] = useState<NotificationCounts>({
    unreadQuotes: 0,
    approachingDeadlines: 0,
    pendingActions: 0,
  });

  const fetchCounts = useCallback(async () => {
    try {
      const res = await fetch("/api/notifications");
      if (res.ok) setCounts(await res.json());
    } catch {
      // ignore
    }
  }, []);

  useEffect(() => {
    fetchCounts();
    const interval = setInterval(fetchCounts, 60000);
    return () => clearInterval(interval);
  }, [fetchCounts]);

  function isActive(href: string) {
    if (href === "/") return pathname === "/";
    if (href === "/rfq") return pathname === "/rfq" || pathname.startsWith("/rfq");
    if (href === "/packages") return pathname === "/packages" || pathname.startsWith("/packages");
    return pathname.startsWith(href);
  }

  function getBadgeCount(badgeKey: string | null): number {
    if (!badgeKey) return 0;
    return counts[badgeKey as keyof NotificationCounts] ?? 0;
  }

  return (
    <>
      {open && (
        <div
          className="fixed inset-0 z-30 bg-black/60 backdrop-blur-sm lg:hidden"
          onClick={onClose}
        />
      )}
      <aside
        className={`fixed top-0 left-0 z-40 flex h-full w-60 flex-col border-r border-border bg-bg-secondary transition-transform duration-200 lg:static lg:translate-x-0 ${open ? "translate-x-0" : "-translate-x-full"}`}
      >
        <div className="flex h-14 items-center gap-3 border-b border-border px-5">
          <div className="flex h-8 w-8 items-center justify-center rounded-md bg-[#25252d] overflow-hidden">
            <Image
              src="/icon-light.png"
              alt="Elite Nexus"
              width={24}
              height={24}
            />
          </div>
          <div>
            <div className="text-sm font-semibold text-text-primary">Bid Elite</div>
            <div className="text-[10px] text-text-dim">RFQ Platform</div>
          </div>
        </div>

        <nav className="flex-1 space-y-1 px-3 py-4">
          {navItems.map((item) => {
            const badge = getBadgeCount(item.badgeKey);
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={onClose}
                className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                  isActive(item.href)
                    ? "bg-accent/10 text-accent"
                    : "text-text-muted hover:bg-bg-elevated hover:text-text-secondary"
                }`}
              >
                {item.icon}
                <span className="flex-1">{item.label}</span>
                {badge > 0 && (
                  <span className="flex h-5 min-w-5 items-center justify-center rounded-full bg-accent px-1.5 text-[10px] font-bold text-white">
                    {badge > 99 ? "99+" : badge}
                  </span>
                )}
              </Link>
            );
          })}
        </nav>

        <div className="border-t border-border p-3">
          <div className="rounded-lg bg-bg-tertiary px-3 py-2 text-[10px] text-text-dim">
            bid.elite-n.com
          </div>
        </div>
      </aside>
    </>
  );
}
